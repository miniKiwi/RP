		<h2>F.A.Q - Frequently Asked Questions</h2>
		Den här sidan kanske behöver skrivas om tydligare/bättre.<br>
		<br>
		<b>Q. Why are there so few languages to choose from?</b><br>
		Endast de språk som administratiorerna och moderatorerna känner att de behärskar går att välja. Detta är för att ingen ska kunna undkomma reglerna eller vara otrevlig utan att någon kan ta hand om det.<br>
		<br>
		<b>Q. Varför finns inte vissa kategorier för rollspel?</b><br>
		Vissa kategorier finns inte att välja för rollspel, pågrund av att administratorer/moderatorer inte känner sig bekväma med att läsa dessa.<br>
